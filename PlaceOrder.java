    private void cartBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cartBTNActionPerformed
        // TODO add your handling code here:

        if (PNameTF.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a product from the list.");
            return;
        }
        String name = PNameTF.getText();
        String price = PPriceTF.getText();
        String quantity = String.valueOf(jSpinner1.getValue());
        DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
        dtm.addRow(new Object[]{name, price, quantity, productTotal});
        totalAmnt += productTotal;
        totalALB.setText(String.valueOf(totalAmnt));

        PNameTF.setText("");
        PPriceTF.setText("");
        jSpinner1.setValue(0);
        PTotalTF.setText("");

        validateFields();
        printBTN.setEnabled(true);
    }//GEN-LAST:event_cartBTNActionPerformed


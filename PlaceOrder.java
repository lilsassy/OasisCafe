    public void productNameByCategory(String category) {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<product> list = productDao.getAllRecordsByCategory(category);
        Iterator<product> itr = list.iterator();
        while (itr.hasNext()) {
            product productObj = itr.next();
            dtm.addRow(new Object[]{productObj.getName()});
        }
    }

    public void filterproductByName(String name, String category) {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<product> list = productDao.filterproductByName(name, category);
        Iterator<product> itr = list.iterator();
        while (itr.hasNext()) {
            product productObj = itr.next();
            dtm.addRow(new Object[]{productObj.getName()});
        }
    }

    public void validateFields() {
        String customerName = nameCDTF.getText();
        String customerNumber = mobileCDTF.getText();
        String customerEmail = emailCDTF.getText();
        boolean isEmailValid = Pattern.matches(emailPattern, customerEmail);

        if (!customerEmail.isEmpty() && customerNumber.matches(mobilePattern) && customerNumber.length() == 11 && isEmailValid && totalAmnt > 0) {
            printBTN.setEnabled(true);
        } else {
            printBTN.setEnabled(false);
        }
    }


    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String ProductName = model.getValueAt(index, 0).toString();
        product product = productDao.getproductByname(ProductName);
        PNameTF.setText(product.getName());
        PPriceTF.setText(product.getPrice());
        jSpinner1.setValue(1);
        PTotalTF.setText(product.getPrice());
        productPrice = (int) Double.parseDouble(product.getPrice());
        productTotal = (int) Double.parseDouble(product.getPrice());

        int quantity = (Integer) jSpinner1.getValue();
        double productTotal = productPrice * quantity;

        PTotalTF.setText(String.format("%.2f", productTotal));
        productTotal = productPrice;
        cartBTN.setEnabled(true);

    }//GEN-LAST:event_jTable1MouseClicked

    private void jSpinner1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinner1StateChanged
        // TODO add your handling code here:

        int quantity = (Integer) jSpinner1.getValue();
        if (quantity < 1) {
            jSpinner1.setValue(1);
            quantity = 1;
        }
        productTotal = productPrice * quantity;
        PTotalTF.setText(String.valueOf(productTotal));

    }//GEN-LAST:event_jSpinner1StateChanged

    private void printBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printBTNActionPerformed
        // TODO add your handling code here:
        if (nameCDTF.getText().isEmpty() || mobileCDTF.getText().isEmpty() || emailCDTF.getText().isEmpty() || totalAmnt <= 0) {
            JOptionPane.showMessageDialog(this, "Please fill in all required fields and add products to the cart.");
            return;
        }
        String customerName = nameCDTF.getText();
        String mobileNumber = mobileCDTF.getText();
        String email = emailCDTF.getText();
        try {
            int billId = Integer.parseInt(receiptDAO.getId());
            Receipt receipt = new Receipt();
            receipt.setId(billId);
            receipt.setName(customerName);
            receipt.setMobileNumber(mobileNumber);
            receipt.setEmail(email);

            SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
            Date date = new Date();
            String todayDate = dFormat.format(date);
            receipt.setDate(todayDate);

            String total = String.valueOf(totalAmnt);
            receipt.setTotal(total);
            String createdBy = userEmail;
            receipt.setCreatedBy(createdBy);
            receiptDAO.save(receipt);

            // create docu
            String userHome = System.getProperty("user.home");
            String pdfFilePath = userHome + File.separator + "Desktop" + File.separator + "receipt_" + billId + ".pdf";

            com.itextpdf.text.Document doc = new com.itextpdf.text.Document();
            PdfWriter.getInstance(doc, new FileOutputStream(pdfFilePath));
            doc.open();
            Paragraph cafeName = new Paragraph("                                                                       Oasis Cafe\n");
            doc.add(cafeName);
            Paragraph starLine = new Paragraph("************************************************************************************************************");
            doc.add(starLine);
            Paragraph paragraph3 = new Paragraph("\tReceipt id: " + billId + "\nCustomer Name: " + customerName + "\nTotal Paid: " + totalAmnt);
            doc.add(paragraph3);
            doc.add(starLine);
            PdfPTable tb1 = new PdfPTable(4);
            doc.add(tb1);
            tb1.addCell("Name");
            tb1.addCell("Price");
            tb1.addCell("Quantity");
            tb1.addCell("Total");
            for (int i = 0; i < jTable2.getRowCount(); i++) {
                String n = jTable2.getValueAt(i, 0).toString();
                String d = jTable2.getValueAt(i, 1).toString();
                String r = jTable2.getValueAt(i, 2).toString();
                String g = jTable2.getValueAt(i, 3).toString();
                tb1.addCell(n);
                tb1.addCell(d);
                tb1.addCell(r);
                tb1.addCell(g);
            }
            doc.add(tb1);
            doc.add(starLine);
            Paragraph tnxMsg = new Paragraph("Thank you! Please Visit Us Again");
            doc.add(tnxMsg);
            doc.close();
            // Open the PDF
            openPdf.openById(pdfFilePath);

            resetForm();
            JOptionPane.showMessageDialog(this, "Order placed successfully!");

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_printBTNActionPerformed

